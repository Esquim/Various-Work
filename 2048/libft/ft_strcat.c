#include <libft.h>

char	*ft_strcat(char *s1, char *s2)
{
	int		pos;

	pos = ft_strlen(s1);
	while (*s2)
	{
		s1[pos] = *s2;
		pos++;
		s2++;
	}
	s1[pos] = '\0';
	return (s1);
}
